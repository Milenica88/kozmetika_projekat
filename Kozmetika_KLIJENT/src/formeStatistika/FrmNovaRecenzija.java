    private void popuni() {
        try {
            ArrayList<Proizvod> proizvodi = KlijentKontroler.getInstance().getAllProizvod();

            cmbProizvod.removeAllItems();

            for (Proizvod proizvod : proizvodi) {
                cmbProizvod.addItem(proizvod);
            }

        } catch (Exception ex) {
            Logger.getLogger(FrmNovaRecenzija.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private void popuniOcene() {
    cmbOcena.removeAllItems(); // Očisti trenutne stavke, ako ih ima.
    for (int i = 1; i <= 5; i++) {
        cmbOcena.addItem(String.valueOf(i)); // Dodaj vrednosti kao stringove.
    }
}
