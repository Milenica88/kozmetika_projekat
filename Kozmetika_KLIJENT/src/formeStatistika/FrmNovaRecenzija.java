    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed
        try {

            Proizvod pr = (Proizvod) cmbProizvod.getSelectedItem();
            String ocena = (String) cmbOcena.getSelectedItem();
            
            int oc = Integer.parseInt(ocena);
            if (oc < 1 || oc > 5) { // Example validation: rating between 1 and 10
                JOptionPane.showMessageDialog(this, "Ocena mora biti između 1 i 5.");
                return;
            }

            Recenzija re = new Recenzija(-1L, pr, k, oc);

            KlijentKontroler.getInstance().addRecenzija(re);

            JOptionPane.showMessageDialog(this, "Sistem je zapamtio recenziju");
            this.dispose();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Sistem ne moze da zapamti recenziju");
        }

    }//GEN-LAST:event_btnSacuvajActionPerformed


